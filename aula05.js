//Operados Relacionais
/*

> Maior
>= Maior ou igual
< Menor
<= Menor ou igual
== Igual
!= Diferente

*/

let num1=10
let num2=5
let num3=10

console.log(num1 > num2)
console.log(num1 < num2)
console.log(num1 >= num3)
console.log(num1 == num3)
console.log(num1 != num3)

