// Pré incremento e Pós incremento

let n=10

// n++ Pós incremento, pega o valor e depois incrementa
// ++n Pré incremento, incrementa primeiro depois pega o valor

console.log(n++) //como é um Pós vai imprimir 10, se fosse ++n iria imprimir 11
console.log(n) //variável já incrementada

let x=-n // - é uma operação de inverção
console.log(x)

console.log(n + "" + x) // O + quando é seguido de string é operador de concatenação