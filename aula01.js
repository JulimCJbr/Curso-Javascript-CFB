/*
 Comandos Essenciais do CMD para Trabalhar com JavaScript e Node.js

 1. Verificar a Instalação do Node.js e npm:
    node -v    # Verifica a versão do Node.js instalada
    npm -v     # Verifica a versão do npm (gerenciador de pacotes do Node.js)

 2. Criar e Executar Arquivos JavaScript:
    node arquivo.js   # Executa um arquivo JavaScript com Node.js

    Exemplo: Se você tem um arquivo app.js, execute:
    node app.js

 3. Inicializar um Projeto Node.js:
    npm init -y  # Cria um package.json padrão automaticamente

 4. Instalar e Gerenciar Pacotes:
    npm install <pacote>       # Instala um pacote localmente
    npm install -g <pacote>    # Instala um pacote globalmente
    npm uninstall <pacote>     # Remove um pacote instalado
    npm update                 # Atualiza pacotes instalados

    Exemplo: Para instalar o Express:
    npm install express

 5. Executar Código Interativamente (REPL):
    node  # Abre um terminal interativo para testar comandos JavaScript

 6. Verificar Processos e Finalizar Aplicações:
    tasklist            # Lista processos em execução
    taskkill /F /IM node.exe   # Finaliza processos do Node.js

 7. Navegar entre Diretórios e Listar Arquivos:
    dir   # Lista todos os arquivos e pastas do diretório atual
    cd <nome_da_pasta>   # Acessa um diretório específico
    cd ..  # Volta para o diretório anterior

 8. Limpar o Terminal:
    cls  # Limpa a tela do CMD (Windows)
    clear  # Em terminais Unix (Linux/macOS)
*/